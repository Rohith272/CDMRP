package com.example.myapplication;

public class registr {
    private String Name;
    private String Gender;
    private String DOB;
    private String Age;
    private String NOP;
    private String Place;
    private String District;
    private String MobNo;
    private String WNo;
    private String Condition;
    private String Currently_Receiving_Therapy;
    private String TherapyList;




    public registr() {
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getGender() {
        return Gender;
    }

    public void setGender(String gender) {
        Gender = gender;
    }

    public String getDOB() {
        return DOB;
    }

    public void setDOB(String DOB) {
        this.DOB = DOB;
    }

    public String getAge() {
        return Age;
    }

    public void setAge(String age) {
        Age = age;
    }

    public String getNOP() {
        return NOP;
    }

    public void setNOP(String NOP) {
        this.NOP = NOP;
    }

    public String getPlace() {
        return Place;
    }

    public void setPlace(String place) {
        Place = place;
    }

    public String getDistrict() {
        return District;
    }

    public void setDistrict(String district) {
        District = district;
    }

    public String getMobNo() {
        return MobNo;
    }

    public void setMobNo(String mobNo) {
        MobNo = mobNo;
    }

    public String getWNo() {
        return WNo;
    }

    public void setWNo(String WNo) {
        this.WNo = WNo;
    }

    public String getCondition() {
        return Condition;
    }

    public void setCondition(String condition) {
        Condition = condition;
    }

    public String getCurrently_Receiving_Therapy() {
        return Currently_Receiving_Therapy;
    }

    public void setCurrently_Receiving_Therapy(String currently_Receiving_Therapy) {
        Currently_Receiving_Therapy = currently_Receiving_Therapy;
    }

    public String getTherapyList() {
        return TherapyList;
    }

    public void setTherapyList(String therapyList) {
        TherapyList = therapyList;
    }

}
